      def load_varuint
        n = (io.getbyte or raise)
        if n < 128
          n
        else
          n -= 128
          shift = 0
          while (b = (io.getbyte or raise)) >= 128
            n += (b - 128) << (shift += 7)
          end
          n + (b << (shift + 7))
        end
