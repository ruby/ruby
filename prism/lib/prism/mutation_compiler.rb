      node.copy(predicate: visit(node.predicate), statements: visit(node.statements))
    end

    #: (WhenNode) -> node?
    def visit_when_node(node) # :nodoc:
      node.copy(conditions: visit_all(node.conditions), statements: visit(node.statements))
    end

    #: (WhileNode) -> node?
    def visit_while_node(node) # :nodoc:
      node.copy(predicate: visit(node.predicate), statements: visit(node.statements))
    end

    #: (XStringNode) -> node?
    def visit_x_string_node(node) # :nodoc:
      node.copy
    end

    #: (YieldNode) -> node?
    def visit_yield_node(node) # :nodoc:
      node.copy(arguments: visit(node.arguments))
    end
  end
