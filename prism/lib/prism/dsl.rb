    # Build the newline byte offset array for the given source string.
    #--
    #: (String source) -> Array[Integer]
    def build_offsets(source)
      offsets = [0]
      start = 0
      while (index = source.byteindex("\n", start))
        offsets << (start = index + 1)
      end
      offsets
    end
