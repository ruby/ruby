static bool
big5_codepoint_match(big5_codepoint_t codepoint, big5_codepoint_t *codepoints, size_t size) {
  for (size_t index = 0; index < size; index += 2) {
    if (codepoint >= codepoints[index] && codepoint <= codepoints[index + 1])
      return true;
  }
  return false;
}

static big5_codepoint_t
big5_codepoint(const char *c, size_t *width) {
  const unsigned char *uc = (const unsigned char *) c;

  if (*uc < 0x80) {
    *width = 1;
    return *uc;
  } else {
    *width = 2;
    return uc[0] << 8 | uc[1];
  }
}

__attribute__((__visibility__("default"))) extern size_t
yp_encoding_big5_alpha_char(const char *c) {
  size_t width;
  big5_codepoint_t codepoint = big5_codepoint(c, &width);

  return big5_codepoint_match(codepoint, big5_alpha_codepoints, BIG5_ALPHA_CODEPOINTS_LENGTH) ? width : 0;
}

__attribute__((__visibility__("default"))) extern size_t
yp_encoding_big5_alnum_char(const char *c) {
  size_t width;
  big5_codepoint_t codepoint = big5_codepoint(c, &width);

  return big5_codepoint_match(codepoint, big5_alnum_codepoints, BIG5_ALNUM_CODEPOINTS_LENGTH) ? width : 0;
}

__attribute__((__visibility__("default"))) extern bool
yp_encoding_big5_isupper_char(const char *c) {
  size_t width;
  big5_codepoint_t codepoint = big5_codepoint(c, &width);

  return big5_codepoint_match(codepoint, big5_isupper_codepoints, BIG5_ISUPPER_CODEPOINTS_LENGTH);
}

