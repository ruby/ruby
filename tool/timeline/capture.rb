#!/usr/bin/env ruby

require 'erb'
require 'optparse'
require 'pathname'

USDT_GROUPS = {
  'default' => [
    ['gc__mark__begin',   "default",  'gc_mark',          'B',  0],
    ['gc__mark__end',     "default",  'gc_mark',          'E',  0],
    ['gc__sweep__begin',  "default",  'gc_sweep',         'B',  0],
    ['gc__sweep__end',    "default",  'gc_sweep',         'E',  0],
    ['gc__enter',         "default",  'GCEnterExit',      'B',  1],
    ['gc__exit',          "default",  'GCEnterExit',      'E',  1],
  ],
  'event_hooks' => [
    ['gc__event_hook',    "ruby",     'gc_event_hook',    'i',  1],
  ],
  'xmalloc' => [
    ['gc__xmalloc',       "ruby",     'gc_xmalloc',       'i',  2],
    ['gc__xcalloc',       "ruby",     'gc_xcalloc',       'i',  2],
  ],
  'xfree' => [
    ['gc__xfree',         "ruby",     'gc_xfree',         'i',  0],
  ],
  'obj_new' => [
    ['gc__obj_new',       "ruby",     'gc_obj_new',       'i',  2],
  ],
  'obj_free' => [
    ['gc__obj_free',      "ruby",     'gc_obj_free',      'i',  2],
  ],
  'gvl' => [
    ['gvl__acquire',      "ruby",     'GVL',              'B',  0],
    ['gvl__release',      "ruby",     'GVL',              'E',  0],
  ],
  'rts' => [ # ractor.thread.sched
    ['rts__set_running',  "ruby",     'rts_set_running',  'i',  2],
  ]
}

DEFAULT_GROUP = USDT_GROUPS['default']
EXTRA_GROUPS = USDT_GROUPS.reject {|k,v| k =='default'}

options = {}

OptionParser.new do |parser|
  parser.accept(Pathname) do |value|
    Pathname.new(value)
  end
  parser.on('-r', '--ruby-bin RUBY', Pathname, 'Path to the ruby executable')
  parser.on('-m', '--gc-module-so GC_MODULE', Pathname, 'Path to the GC module .so')
  parser.on('-e', '--erb-debug', TrueClass, 'Print the Ruby script generated by ERB.  For debugging.')
  parser.on('-p', '--print-script', TrueClass, 'Print the content of the bpftrace script')
  parser.on('-d', '--dry-run', TrueClass, 'Dry run.  Print the bpftrace command to be executed, but do not actually execute it.')
  parser.on('-g', '--extra-groups=', 'Enable extra USDT groups, separated by comma.  Available groups: ' + EXTRA_GROUPS.keys.join(', '))
end.parse!(into: options)

[:'ruby-bin'].each do |k|
  if !options.include?(k)
    raise "Option --#{k} is required"
  end
end

ruby_bin = options[:'ruby-bin']
if !ruby_bin.file?
  raise "Ruby executable '#{ruby_bin}' does not exist"
end

gc_module_so = options[:'gc-module-so']

gc_module_kind = if gc_module_so.nil?
  nil
elsif m = /librubygc\.(\w+)/.match(gc_module_so.basename.to_s)
  m[1]
else
  raise "Unexpected gc module so name: #{gc_module_so}"
end

script_path = Pathname(__dir__) / 'capture.bt'
script = File.read(script_path)
template = ERB.new(script, trim_mode: '%<>')
template.location = [script_path.to_s, 1]

if options[:'erb-debug']
  puts template.src
end

usdts = []
usdts += DEFAULT_GROUP

extra_groups_opt = options[:'extra-groups']
puts "Extra groups: #{extra_groups_opt}"

if !extra_groups_opt.nil?
  extra_groups_opt.split(',').each do |group|
    if EXTRA_GROUPS.include?(group)
      usdts += EXTRA_GROUPS[group]
    else
      raise "Unknown USDT group #{group}"
    end
  end
end

content = template.result_with_hash({
  ruby_bin: ruby_bin,
  gc_module_so: gc_module_so,
  gc_module_kind: gc_module_kind,
  usdts: usdts,
})

if options[:"print-script"]
  $stderr.puts content
end

IO.write('capture.out.bt', content)

command_line = ["sudo", "bpftrace", "-v", "--unsafe", "capture.out.bt"]
$stderr.puts "Command to execute:"
$stderr.puts command_line.map{|s| "'#{s}'"}.join(' ')

if options[:"dry-run"]
  $stderr.puts "Dry run.  Exit..."
  exit 0
end

exec(*command_line)
