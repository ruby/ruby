#!/usr/bin/env ruby

require 'erb'
require 'optparse'
require 'pathname'

USDT_GROUPS = {
  'default' => [
    ['gc__mark__begin',   'gc_mark',          'B',  0],
    ['gc__mark__end',     'gc_mark',          'E',  0],
    ['gc__sweep__begin',  'gc_sweep',         'B',  0],
    ['gc__sweep__end',    'gc_sweep',         'E',  0],
    ['gc__enter',         'GCEnterExit',      'B',  1],
    ['gc__exit',          'GCEnterExit',      'E',  1],
  ],
  'event_hooks' => [
    ['gc__event_hook',    'gc_event_hook',    'i',  1],
  ],
  'xmalloc' => [
    ['gc__xmalloc',       'gc_xmalloc',       'i',  2],
    ['gc__xcalloc',       'gc_xcalloc',       'i',  2],
  ],
  'xfree' => [
    ['gc__xfree',         'gc_xfree',         'i',  0],
  ],
}

DEFAULT_GROUP = USDT_GROUPS['default']
EXTRA_GROUPS = USDT_GROUPS.reject {|k,v| k =='default'}

options = {}

OptionParser.new do |parser|
  parser.accept(Pathname) do |value|
    Pathname.new(value)
  end
  parser.on('-r', '--ruby RUBY', Pathname, 'Path to the ruby executable')
  parser.on('-e', '--erb-debug', TrueClass, 'Print the Ruby script generated by ERB.  For debugging.')
  parser.on('-p', '--print-script', TrueClass, 'Print the content of the bpftrace script')
  parser.on('-d', '--dry-run', TrueClass, 'Dry run.  Print the bpftrace command to be executed, but do not actually execute it.')
  parser.on('-g', '--extra-groups=', 'Enable extra USDT groups, separated by comma.  Available groups: ' + EXTRA_GROUPS.keys.join(', '))
end.parse!(into: options)

[:ruby].each do |k|
  if !options.include?(k)
    raise "Option --#{k} is required"
  end
end

ruby_bin = options[:ruby]
if !ruby_bin.file?
  raise "Ruby executable '#{ruby_bin}' does not exist"
end

script_path = Pathname(__dir__) / 'capture.bt'
script = IO.read(script_path)
template = ERB.new(script, trim_mode: '%<>')
template.location = [script_path.to_s, 1]

if options[:'erb-debug']
  puts template.src
end

usdts = []
usdts += DEFAULT_GROUP

extra_groups_opt = options[:'extra-groups']
puts "Extra groups: #{extra_groups_opt}"

if !extra_groups_opt.nil?
  extra_groups_opt.split(',').each do |group|
    if EXTRA_GROUPS.include?(group)
      usdts += EXTRA_GROUPS[group]
    else
      raise "Unknown USDT group #{group}"
    end
  end
end

content = template.result_with_hash({
  ruby: ruby_bin,
  usdts: usdts,
})

if options[:"print-script"]
  $stderr.puts content
end

IO.write('capture.out.bt', content)

command_line = ["sudo", "bpftrace", "-v", "--unsafe", "capture.out.bt"]
$stderr.puts "Command to execute:"
$stderr.puts command_line.map{|s| "'#{s}'"}.join(' ')

if options[:"dry-run"]
  $stderr.puts "Dry run.  Exit..."
  exit 0
end

exec(*command_line)
